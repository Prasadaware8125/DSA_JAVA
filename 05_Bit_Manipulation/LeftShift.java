// It shifts all bits to the left hand side (<<)
public class LeftShift {
    public static void main(String[] args) {
        // IMP Formula 
        // a << b = a * 2^b
        // eg. a = 5 and b = 2 the 5 << 2 = 5 * 2 ^2 = 20 
        System.out.println(5<<2);
        System.out.println(3<<2);
    }
}
